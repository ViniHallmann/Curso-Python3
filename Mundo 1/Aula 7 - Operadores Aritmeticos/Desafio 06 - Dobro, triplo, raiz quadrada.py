#Crie um algoritmo que leia um número 
#e mostre o seu dobro, seu triplo e sua raiz quadrada

a = int (input ('Digite um número qualquer:'))
print(f'O número escolhido é {a}')
print(f'O dobro do seu número é {a*2}, o triplo do seu número é {a*3}, e a raiz quadrada do seu número é {a**0.5:.2f}')